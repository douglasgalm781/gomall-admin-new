export const DICT = { en, zh };
export const LANGUAGES = [
  { code: "zh", label: "中文" },
  { code: "en", label: "English" },
];

const DEFAULT_LANG = "zh";
const STORAGE_KEY = "gomall_admin_lang";

const I18nContext = createContext({ lang: DEFAULT_LANG, setLang: () => {}, t: (k) => k });

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState(DEFAULT_LANG);
  useEffect(() => {
    try { const saved = localStorage.getItem(STORAGE_KEY); if (saved) setLangState(saved); } catch {}
  }, []);
  const setLang = (code) => {
    try { localStorage.setItem(STORAGE_KEY, code); } catch {}
    setLangState(code);
  };
  const t = (key, vars) => {
    let s = DICT[lang]?.[key] ?? DICT.en[key] ?? key;
    if (vars) for (const k in vars) s = s.split(`{${k}}`).join(String(vars[k]));
    return s;
  };
  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

export function useI18n() { return useContext(I18nContext); }
